package com.javabasics;

public class Demo2 {

	public static void main(String[] args) {

		System.out.println("While Loop");
		int i = 5;
		while (i < 10) {
			System.out.println(i);
			i++;
		}

		System.out.println("Do While loop");
		int j = 1;
		do {
			System.out.println(j);
			j++;
		} while (j < 10);
	}
}
